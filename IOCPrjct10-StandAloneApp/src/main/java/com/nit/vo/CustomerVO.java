package com.nit.vo;

public class CustomerVO {
	private String cno;
	private String cname;
	private String cadd;
	private String pamnt;
	private String rate;
	private String time;
	
	//setters and getters
	public String getCno() {
		return cno;
	}
	public void setCno(String cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCadd() {
		return cadd;
	}
	public void setCadd(String cadd) {
		this.cadd = cadd;
	}
	public String getPamnt() {
		return pamnt;
	}
	public void setPamnt(String pamnt) {
		this.pamnt = pamnt;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}

	
	
}
