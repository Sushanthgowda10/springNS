package com.nit.vo;

public class PatientVO {
	private String pno;
	private String pname;
	private String padd;
	private String pdchrg;
	private String days;
	private String tamt;

	// setters and getters
	public String getPno() {
		return pno;
	}

	public void setPno(String pno) {
		this.pno = pno;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pname) {
		this.pname = pname;
	}

	public String getPadd() {
		return padd;
	}

	public void setPadd(String padd) {
		this.padd = padd;
	}

	public String getPdchrg() {
		return pdchrg;
	}

	public void setPdchrg(String pdchrg) {
		this.pdchrg = pdchrg;
	}

	public String getDays() {
		return days;
	}

	public void setDays(String days) {
		this.days = days;
	}
	public String getTamt() {
		return tamt;
	}
	public void setTamt(String tamt) {
		this.tamt = tamt;
	}
	

}
